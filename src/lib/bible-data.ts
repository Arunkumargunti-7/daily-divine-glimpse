// Generate random Psalm verses for the day
export const getRandomPsalmVerses = (language = 'en') => {
  const date = new Date();
  // Create a seed based on the date (changes daily)
  const seed = date.getFullYear() * 10000 + (date.getMonth() + 1) * 100 + date.getDate();
  
  // Use the seed to determine which psalm to use today
  const psalmKeys = Object.keys(language === 'en' ? psalmVerses : psalmVersesES);
  const psalmIndex = seed % psalmKeys.length;
  const selectedPsalmKey = psalmKeys[psalmIndex];
  
  const selectedPsalm = language === 'en' 
    ? psalmVerses[selectedPsalmKey as keyof typeof psalmVerses]
    : psalmVersesES[selectedPsalmKey as keyof typeof psalmVersesES] || psalmVerses[selectedPsalmKey as keyof typeof psalmVerses];
  
  // Return the full psalm
  return {
    verses: selectedPsalm,
    reference: `Psalm ${selectedPsalmKey}`
  };
};

// Generate random Bible promise verse for the day
export const getRandomBiblePromiseVerse = (language = 'en') => {
  const date = new Date();
  // Create a seed based on the date (changes daily)
  const seed = date.getFullYear() * 10000 + (date.getMonth() + 1) * 100 + date.getDate();
  
  const promises = language === 'en' ? biblePromiseVerses : biblePromiseVersesES;
  const verseIndex = seed % promises.length;
  
  return promises[verseIndex];
};
